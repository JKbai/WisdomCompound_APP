#pragma mark 正则匹配手机号
+ (BOOL) checkMobile:(NSString *)mobileNumbel{
    
    NSString * REGEX_MOBILE_EXACT  = @"^((13[0-9])|(14[5,7])|(15[0-3,5-9])|(17[0,1,3,5-8])|(18[0-9])|(147))\\d{8}$";
    NSPredicate *regextestmobile = [NSPredicate predicateWithFormat:@"SELF MATCHES %@", REGEX_MOBILE_EXACT];
    
    if (([regextestmobile evaluateWithObject:mobileNumbel]
         ) && (mobileNumbel.length == 11)) {
        
        return YES;
    }
    return NO;
    
}
#pragma mark 正则匹配用户身份证号15或18位
+(BOOL)checkUserIdCard :(NSString *)idCard{
    
    NSString *pattern = @"(^[0-9]{15}$)|([0-9]{17}([0-9]|X)$)";
    NSPredicate *pred = [NSPredicate predicateWithFormat:@"SELF MATCHES %@",pattern];
    BOOL isMatch = [pred evaluateWithObject:idCard];
    return isMatch;
}
#pragma mark 正则匹配用户密码6-20位数字和字母组合
+ (BOOL)checkPassword:(NSString *) password
{
    NSString *pattern = @"^(?![0-9]+$)(?![a-zA-Z]+$)[a-zA-Z0-9]{6,20}";
    NSPredicate *pred = [NSPredicate predicateWithFormat:@"SELF MATCHES %@", pattern];
    BOOL isMatch = [pred evaluateWithObject:password];
    return isMatch;
    
}
